-- POSTGRESQL

CREATE TABLE LOGS_ERROR (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ENTRY_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    ERROR_TYPE VARCHAR(255) NOT NULL,
    STACK_TRACE CLOB NOT NULL,
    MSG VARCHAR2(4000) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE LOGS_GENERAL (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ENTRY_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    TYPE VARCHAR(255) NOT NULL,
    MSG VARCHAR2(4000) NOT NULL,
    PRIMARY KEY (ID)
);

-- COACHING INTERNAL
CREATE TABLE LOGS_COACHING_I (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ENTRY_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    USER_ID VARCHAR(20) NOT NULL, CHECK (LENGTH(USER_ID) > 17),
    ACTION VARCHAR(255) NOT NULL,
    MSG VARCHAR2(4000) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE LOGS_ENTRY (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ENTRY_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    USER_ID VARCHAR(20) NOT NULL CHECK (LENGTH(USER_ID) > 17),
    MSG VARCHAR2(4000) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE LOGS_COACHING (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ENTRY_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    USER_ID VARCHAR(20) NOT NULL, CHECK (LENGTH(USER_ID) > 17),
    MSG VARCHAR2(4000) NOT NULL,
    PRIMARY KEY (ID)
);